C     WRITTEN BY DAISUKE TAKAHASHI
C
      IMPLICIT REAL*8 (A-H,O-Z)
      PARAMETER (NDA=16777216)
      DIMENSION A(NDA),B(NDA)
      SAVE A,B
C
      WRITE(6,*) ' NX,NY ='
      READ(5,*) NX,NY
C
      CALL INIT(A,NX*NY)
C
      CALL DZFFT2D(A,NX,NY,0,B)
      CALL DZFFT2D(A,NX,NY,-1,B)
      CALL DUMP(A,(NX/2+1)*NY)
C
      CALL ZDFFT2D(A,NX,NY,0,B)
      CALL ZDFFT2D(A,NX,NY,1,B)
      CALL RDUMP(A,NX*NY)
C
      STOP
      END
      SUBROUTINE INIT(A,N)
      IMPLICIT REAL*8 (A-H,O-Z)
      DIMENSION A(*)
C
      DO 10 I=1,N
        A(I)=DBLE(I)
   10 CONTINUE
      RETURN
      END
      SUBROUTINE DUMP(A,N)
      IMPLICIT REAL*8 (A-H,O-Z)
      COMPLEX*16 A(*)
C
      DO 10 I=1,N
        WRITE(6,*) I,A(I)
   10 CONTINUE
      RETURN
      END
      SUBROUTINE RDUMP(A,N)
      IMPLICIT REAL*8 (A-H,O-Z)
      DIMENSION A(*)
C
      DO 10 I=1,N
        WRITE(6,*) I,A(I)
   10 CONTINUE
      RETURN
      END
