      use cudafor
      IMPLICIT REAL*8 (A-H,O-Z)
      PARAMETER (NDA=33554432)
      complex(8),pinned,allocatable :: A(:)
C
      ALLOCATE(A(NDA))
      WRITE(6,*) ' NX,NY ='
      READ(5,*) NX,NY
C
      CALL INIT(A,NX*NY)
      CALL ZFFT2D(A,NX,NY,0)
C
      CALL ZFFT2D(A,NX,NY,-1)
      CALL DUMP(A,NX*NY)
C
      CALL ZFFT2D(A,NX,NY,1)
      CALL DUMP(A,NX*NY)
C
      CALL ZFFT2D(A,NX,NY,3)
      DEALLOCATE(A)
C
      STOP
      END
      SUBROUTINE INIT(A,N)
      IMPLICIT REAL*8 (A-H,O-Z)
      COMPLEX*16 A(*)
C
!DIR$ VECTOR ALIGNED
      DO 10 I=1,N
        A(I)=DCMPLX(DBLE(I),DBLE(N-I+1))
   10 CONTINUE
      RETURN
      END
      SUBROUTINE DUMP(A,N)
      IMPLICIT REAL*8 (A-H,O-Z)
      COMPLEX*16 A(*)
C
      DO 10 I=1,N
        WRITE(6,*) I,A(I)
   10 CONTINUE
      RETURN
      END
